    private void txtTienDuaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTienDuaKeyReleased
        // TODO add your handling code here:  
        String cautruyvan = "select * from HoaDon where MaHoaDon = "+MaHoaDon;
        ResultSet rs = cnn.ExcuteQueryGetTable(cautruyvan);
        try {
            while(rs.next())
            {
                long TienTra = rs.getLong("TongTien");
                try
                {
                    String TienKhachDua = txtTienDua.getText();
                    
                    long m =Long.valueOf(TienKhachDua)-TienTra;
                    Locale localeVN = new Locale("vi", "VN");
                    NumberFormat currencyVN = NumberFormat.getCurrencyInstance(localeVN);
                    String str2 = currencyVN.format(m);
                    txtTienThua.setText(str2);
                    if(m<0)
                    {
                        lablecc.setText("Số tiền nhận chưa đủ");
                        lablecc.setForeground(Color.red);
                    }
                    else
                    {

                        lablecc.setText("Đã nhận đủ số tiền");
                        lablecc.setForeground(Color.BLUE);
                    }
                }catch(NumberFormatException nn)
                {
                    ThongBao("Tiền là số", "Thông báo", 2);

                }          
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmThanhToan.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_txtTienDuaKeyReleased

