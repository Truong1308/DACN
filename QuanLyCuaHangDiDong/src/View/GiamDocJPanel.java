    public void HienHeSo()
    {
        String cautruyvan = "select SanPham.TenSanPham,HeSo,PhieuNhap.NgayNhap "
                + "from SanPham,PhieuNhap,ChiTietPhieuNhap "
                + "where SanPham.MaSanPham = ChiTietPhieuNhap.MaSanPham "
                + "and PhieuNhap.MaPhieuNhap = ChiTietPhieuNhap.MaPhieuNhap ";
        ResultSet rs = cnn.ExcuteQueryGetTable(cautruyvan);
        Object[] obj = new Object[]{"STT", "Tên Sản Phẩm","Ngày Nhập","Hệ Số"};
        DefaultTableModel tableModel = new DefaultTableModel(obj, 0);
        tblHeSo.setModel(tableModel);
        int c = 0;
        try {
            while (rs.next()) {
                c++;
                Object[] item = new Object[4];
                item[0] = c;
                item[1] = rs.getString("TenSanPham");
                item[2] = rs.getString("NgayNhap");
                item[3] = rs.getString("HeSo"); 
                tableModel.addRow(item);
            }
        } catch (SQLException ex) {
            System.out.println(ex.toString());
        }
    }
    
    
