    private void btnXoaLoaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaLoaiActionPerformed
        // TODO add your handling code here:

        String TenLoaiKT = null;
        String MaLoaiKH = txtMaLoaiKhachHang.getText();
        if(MaLoaiKH.equals(""))
        {
            ThongBao("Bạn chưa chọn Loại KH cần sửa", "Thông báo", 2);
        }
        else
        {
            String TenLoai = txtLoaiKhach.getText();
            String ctv = "select KhachHang.LoaiKhachHang as Loai from KhachHang,LoaiKhachHang where TenLoaiKhachHang like N'%" + TenLoai + "%'";
            ResultSet rs = cnn.ExcuteQueryGetTable(ctv);
            try {
                while(rs.next())
                {
                    TenLoaiKT = rs.getString("Loai");
                }
            } catch (SQLException ex) {
                Logger.getLogger(KhachHangJPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(TenLoai.equals(TenLoaiKT))
            {
                ThongBao("Không thể xóa vì có KH đang là loại này", "Thông báo", 2);
            }else
            {
                String cautruyvan = "delete LoaiKhachHang where MaLoaiKhachHang = "+MaLoaiKH;
                cnn.ExcuteQueryUpdateDB(cautruyvan);
                ThongBao("Xóa thành công", "Thông báo", 2);
                txtMaLoaiKhachHang.setText("");
                txtLoaiKhach.setText("");
                txtPhanTram.setText("");
                txtGhiChuLoaiKH.setText("");
            }
            layDuLieuLoaiKhachHang();
        }

    }//GEN-LAST:event_btnXoaLoaiActionPerformed

