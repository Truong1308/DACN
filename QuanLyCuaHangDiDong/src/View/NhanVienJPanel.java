    public boolean KiemTraNhapNhanVien(int ts) 
    {
        String MaNhanVien, TenNhanVien, DiaChi, SDT;
        boolean kiemtra = false;
        MaNhanVien = txtMaNV.getText();
        TenNhanVien = txtTenNV.getText();
        String  ThongBao = "";
        DiaChi = txtDiaChi.getText();
        SDT = txtSDT.getText();
        if (MaNhanVien.equals("") && ts == 1) {
            ThongBao += "bạn chưa chọn khách hàng để lấy  Mã Khách Hàng\n";
            lblMaNV.setForeground(Color.red);
        }
        if (TenNhanVien.equals("")) {
            ThongBao += "bạn chưa Nhập Tên Nhân Viên\n";
            lblTenNV.setForeground(Color.red);
        }
        if (DiaChi.equals("")) {
            lblDiaCHi.setForeground(Color.red);
            ThongBao += "bạn chưa Nhập Địa Chỉ\n";
        }
        if (SDT.equals("")) {
            lblSDT.setForeground(Color.red);
            ThongBao += "bạn chưa Nhập Số ĐT \n";
        }
        if (ThongBao.equals("")) {
            kiemtra = true;
            lblMaNV.setForeground(Color.black);
            lblSDT.setForeground(Color.black);
            lblTenNV.setForeground(Color.black);
            lblDiaCHi.setForeground(Color.black);
        } else {
            kiemtra = false;
            ThongBao(ThongBao, "lỗi nhập liệu", 2);
        }
        return kiemtra;
    }
