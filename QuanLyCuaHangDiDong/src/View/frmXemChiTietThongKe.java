    public void LayThongKe()
    {
        int Thang = Integer.valueOf(th);
        int Nam = Integer.valueOf(nam);
        String cautruyvan ="select  SanPham.TenSanPham, sum(ChiTietThongKe.SoLuongBan) as SoLuong from ChiTietThongKe,SanPham where SanPham.MaSanPham = ChiTietThongKe.MaSanPham and YEAR(NgayBan) = "+Nam+" and MONTH(NgayBan) = "+Thang+" group by TenSanPham";
        ResultSet rs = cnn.ExcuteQueryGetTable(cautruyvan);
        Object[] obj = new Object[]{"STT", "Tên Sản Phẩm", "Số Lượng Bán Được"};
        DefaultTableModel tableModel = new DefaultTableModel(obj, 0);
        tblThongKeChiTiet.setModel(tableModel);
        int c = 0;
        try {
            while (rs.next()) {
                c++;
                Object[] item = new Object[3];
                item[0] = c;
                
                item[1] = rs.getString("TenSanPham");
                item[2] = rs.getInt("SoLuong");
                tableModel.addRow(item);
            }
        } catch (SQLException ex) {
            System.out.println(ex.toString());
        }
    }
